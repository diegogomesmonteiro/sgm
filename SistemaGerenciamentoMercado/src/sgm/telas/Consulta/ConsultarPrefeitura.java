    private void apSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apSalvarActionPerformed
        PessoaFisicaDAO pessoaFisicaDAO = new PessoaFisicaDAO();
        PrefeituraDAO prefeituraDAO = new PrefeituraDAO();
        String cpf = apRL.getText();
        PessoaFisica representanteLegal = pessoaFisicaDAO.consultar(cpf);
        if(representanteLegal == null){
            JOptionPane.showMessageDialog(null, "Representante Legal nÃ£o cadastrado. "
                + "Cadastre os dados do Representante Legal.");
            JDesktopPane desktop = this.getDesktopPane();
            CadastrarPF telaRepresentanteLegal = new CadastrarPF(cpf);
            desktop.add(telaRepresentanteLegal);
            telaRepresentanteLegal.setVisible(true);
            return;
        }
        Prefeitura prefeitura = new Prefeitura(apCnpj.getText(), apNome.getText(), apNomef.getText(),
            apEndereco.getText(), representanteLegal);
        prefeituraDAO.alterar(prefeitura);
        JOptionPane.showMessageDialog(null, "Prefeitura: \n" + prefeitura + "\n Atualizada com sucesso!");
        this.dispose();
    }//GEN-LAST:event_apSalvarActionPerformed

