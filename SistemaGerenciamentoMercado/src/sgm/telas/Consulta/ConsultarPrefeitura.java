    private void apExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apExcluirActionPerformed
        PrefeituraDAO prefeituraDAO = new PrefeituraDAO();
        Prefeitura prefeitura = prefeituraDAO.consultar(this.apCnpj.getText());
        try {  
            prefeituraDAO.excluir(prefeitura.getCnpj());
            JOptionPane.showMessageDialog(null, "Prefeitura: \n" + prefeitura + "\n ExcluÃ­da com sucesso!");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, ex.getMessage());
            this.dispose();
        }
    }//GEN-LAST:event_apExcluirActionPerformed


